#define DMA_BUFFER_BASE_ADDR 0x40300000
#define DMA_BUFFER_SIZE 0x00100000 // 1MB DMA 缓冲区

#define HEAP_BASE_ADDR 0x40400000
#define HEAP_SIZE 0x00C00000 // 12MB 堆空间

